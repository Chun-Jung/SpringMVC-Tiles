package com.tob.model;

public class Auth {
	/** 權限編號 */
	private Integer authNo;
	/** 權限名稱 */
	private String name;
	
	public Integer getAuthNo() {
		return authNo;
	}
	public void setAuthNo(Integer authNo) {
		this.authNo = authNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
